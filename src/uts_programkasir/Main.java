     private void selectData() {
        String kolom[] = {"nomor", "NamaKasir", "TanggalBeli", "NamaItem", "Harga", "Diskon", "Bayar"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom); 
        String SQL = "SELECT * FROM tb_transaksi";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()){
              String nomor = rs.getString(1);
              String NamaKasir = rs.getString(2);
              String TanggalBeli = rs.getString(3);
              String NamaItem = rs.getString(4);
              String Harga = rs.getString(5);
              String Diskon = rs.getString(6);
              String Bayar = rs.getString(7);
              String data[] = {nomor, NamaKasir, TanggalBeli, NamaItem, Harga, Diskon, Bayar};
              dtm.addRow(data);
            }
        }catch (SQLException ex){
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        tb1.setModel(dtm);
    }
    
