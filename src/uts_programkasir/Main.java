    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    String tanggal = dateFormat.format(tglbeli.getDate());
    hrgbrg = Integer.parseInt(totalharga.getText());
    if(hrgbrg >= 200000){
    disk = hrgbrg * 10 / 100;
    tohar = hrgbrg - disk;
    }else{
    disk = 0;
    tohar = hrgbrg - disk;
    }
    String SQL = "INSERT INTO tb_transaksi (nomor,NamaKasir,TanggalBeli,NamaItem,Harga,Diskon,Bayar) VALUES("+kode.getText()+",'"+namakasir.getText()+"','"+tanggal+"','"+namaitem.getText()+"',"+totalharga.getText()+","+disk+","+tohar+");";
    int status = KoneksiDB.execute(SQL);
    if (status == 1){
        JOptionPane.showMessageDialog(this, "Data berhasil dimasukkan", "Berhasil ,We did it", JOptionPane.INFORMATION_MESSAGE);
        selectData();
    }else{
        JOptionPane.showMessageDialog(this, "Data gagal dimasukkan", "Coba Lagi Ya! :*", JOptionPane.WARNING_MESSAGE);
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed

